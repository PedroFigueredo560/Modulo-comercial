    private boolean validaCampos(){
        
        if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"the Name field cannot be blank");
            return false;
        }
        if(txtCode.getText().equals("")){
            JOptionPane.showMessageDialog(null,"the Code field cannot be blank");
            return false;
        }
        if(txtStock.getText().equals("")){
            JOptionPane.showMessageDialog(null,"the Stock field cannot be blank");
            return false;
        } 
        if(txtValue.getText().equals("")){
            JOptionPane.showMessageDialog(null,"the Value field cannot be blank");
            return false;
        }
        
        return true;
    }
    
