    private void jButtonSaveMKSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveMKSActionPerformed
        // TODO add your handling code here:
        int quantia = Integer.parseInt(txtStock.getText());
        int codigo = Integer.parseInt(txtCode.getText());
        float valor= Float.parseFloat(txtValue.getText());
       if(validaCampos() == true){
            prod.setName(txtName.getText());
            prod.setQuantidade(quantia);
            prod.setCod(codigo);
            prod.setValue(valor);
  
        //salva o produto
            prod = FacadeInstance.getInstance().saveProduto(prod);
            if (prod != null) {
                this.produtos.add(prod);
                this.produtos = FacadeInstance.getInstance().getAllProdutos();
                JOptionPane.showMessageDialog(null, "Product save successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Error saving the product");
            }
        //limpa os campos
            txtName.setText("");
            txtStock.setText("");
            txtCode.setText("");
            txtValue.setText("");
       } 
    }//GEN-LAST:event_jButtonSaveMKSActionPerformed

